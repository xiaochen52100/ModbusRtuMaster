/*
    CRC校验
*/
uint16_t usMBCRC16(uint8_t *ptr, uint32_t len)
{
    uint8_t crchi = 0xff;
	uint8_t crclo = 0xff; 
	uint16_t index;
	uint8_t ch;
	int i=0;
	while (len--) 
	{
		ch = *ptr++;
		index = crclo ^ ch; 
		crclo = crchi ^ crctablehi[index];
		crchi = crctablelo[index];
		i++;
	}
	return (crchi << 8 | crclo);

}
/*
    生成modbusrtu读取保持寄存器命令的字节数组，通过串口，tcp或者蓝牙发送字节数组命令
    sendbuf:最终要发送的字节数组
    sendlen:最终要发送的字节数组长度
    station:目标站号
    address:目标地址
    len    :目标长度
*/
void read_mudbus_holding_register_massage(uint8_t *sendbuf,uint16_t sendlen,uint8_t station,uint16_t address,uint16_t len)
{
    sendbuf[0]=station;
    sendbuf[1]=3;
    sendbuf[2]=(address>>8)&0xff;
    sendbuf[3]=address&0xff;
    sendbuf[4]=(len>>8)&0xff;
    sendbuf[5]=len&0xff;
    uint16_t crc=usMBCRC16(sendbuf,6);
    sendbuf[6]=crc&0xff;
    sendbuf[7]=(crc>>8)&0xff;
    sendlen=8;
}
